<template>
  <div>
    <h1>My To Do List</h1>
    </br>
    <input v-model="newItem" type="text">
    <button @click="addItemToList" name="add">Add</button>
    <ul>
      <li 
        v-for="(item, index) in listItems"
        :key="index"
        >{{ item }}</li>
    </ul>
  </div>
</template>

<script>
export default {
  name: 'list',
  data () {
    return {
      listItems: ['buy food', 'play games', 'sleep'],
      newItem: ''
    }
  },
  methods: {
    addItemToList () {
      this.listItems.push(this.newItem)
      this.newItem = ''
    }
  }
}
</script>